    private void setDate()
    {
        Calendar calender =  Calendar.getInstance();
        calender.set(Calendar.YEAR, Year);
        calender.set(Calendar.MONTH, Month-1); //To set the month January as Zero
        calender.set(Calendar.DATE, 1);
        int StartDay = calender.get(Calendar.DAY_OF_WEEK) - 1;
        calender.add(Calendar.DATE, -StartDay);
        ToDay toDay = getToday();
        for(Component comp: getComponents())
        {
           Cells cell = (Cells) comp;
           if(!cell.isTitle())
           {
               cell.setText(calender.get(Calendar.DATE)+ "");
               cell.setDate(calender.getTime());
               cell.currentMonth(calender.get(Calendar.MONTH) == Month - 1);
               if(toDay.isToDay(new ToDay(calender.get(Calendar.DATE), calender.get(Calendar.MONTH)+1 , calender.get(Calendar.YEAR))))
               {
                   cell.setdaytoday();
               }
               calender.add(Calendar.DATE, 1);  //Increase date by one
           }
        }
    }

